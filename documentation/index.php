          <p>Students can step the hours forward and backward by hours.</p>
          <p><strong>All times are in local Irish time or Irish Standard Time (IST).</strong></p>
          <br>
          <div class="text-center">
            <img src="https://i.img.ie/sxs.png" alt="sqj.png" border="0" class="img-responsive center-block"/>
