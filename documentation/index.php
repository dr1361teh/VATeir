      <h6 id="1.2.1">1.2.1 What is it?</h6>
      <p>The first step to getting mentoring in VATeir is to submit your availability to us. Your availability lets us know when you will be around and available, and lets us know exactly the times that you are free so that that we can schedule a mentoring session with you.</p>

