      <h6 id="2.4.3">2.4.3 Student's Validations</h6>
      <p>A student's <i>Validations</i> page can be accessed either by clicking on their name in the Validations list, or by following the <a href="#2.3.4.1">link in their training details</a>.</p>
      <p>The page includes details of positions on which the student is validated to control, the mentor who issued the validation, the date from when the validation takes effect, the date of expiry of the validation and options.</p>
      <p>To <strong>extend</strong> a validation, click on the <span class="glyphicon glyphicon-refresh" aria-hidden="true"></span> icon besidee the validatoin which you would like to extend.</p>
      <p>To <strong>delete</strong> a validation, click on the <span class="glyphicon glyphicon-remove" aria-hidden="true"></span> icon besidee the validatoin which you would like to delete.</p>
      <p>To add a validation, click on the relevant sector from the menu on the right. This will validate the student on all positions <strong>up</strong> to that sector at the chosen airfield. For example, by clicking on <code>APP</code> under Dublin, this will validate the student on DEL, GND, TWR, and APP at once. If the student already has validations on the lower sectors, these validations are extended to the lifetime of the upper validation.</p>
      <p>Students are validated for <strong>60 days</strong>, after which their validation must renewed, otherwise they will be no longer validated.</p>
      <p>Rows which are orange in colour indicate validations which haven't been approved by TD Staff. As such, validations which are orange do not count as validations. In the example below, the student must wait for the training department to approve the validation for <code>EIDW_APP</code> before they are validated.</p>
