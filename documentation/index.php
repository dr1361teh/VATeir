  <h5 id="1.5">1.5 Training History</h5>
  <div class="panel panel-default">
    <div class="panel-body">
      <h6 id="1.5.1">1.5.1 What is it?</h6>
      <p>The <i>Training History</i> page is the location where all of the mentoring reports, notes, and other training-related errata which pertain to you as a student are stored. Other students do not have access to this page, and only VATeir mentors with the requisite permissions and VATeir staff have access to your history.</p>
      <p>At the top of this page is the <i>Training Details</i> section, wherein you will find details including your current ATC Rating, Pilot Rating, VATSIM and VATeir registration dates, Activity Status on the Network and on the VATeir website, Current Training Programme, and the type of controller that you are (Home Controller or Visiting Controller).</p>
      <br>
      <div class="text-center">
        <img src="https://i.img.ie/sqR.png" alt="sqR.png" border="0" class="img-responsive center-block"/>
        <br>
        <strong>Fig. 1.5.1: Training Details</strong>
        <br>
        <hr>
      </div>
      <h6 id="1.5.2">1.5.2 Deck of Cards</h6>
      <p>The method of storing mentoring reports, notes, and information related to sessions is through the use of a 'deck of cards'. For each mentoring report, a card is added to your training history with information contained thereon, including the mentor's comments, and progression through the training syllabus.</p>
      <p>The deck of cards is colour-coded to speed up recognition of a particular card type. The colours of the various cards are given below.</p>
      <p>After a number of sessions, you may notice that the deck of cards begins to fill up as mentors add more and more records to your training history.</p>
      <p>One important point to note is that a card will only appear once the mentor has added a report or a note to your training history. These reports and notes are not system-generated and it might take a number of days before the mentor gets around to filling out a report for a particular session.</p>
      <p>Cards are ordered by date from bottom to top, which means that the most recent card will always be at the top of the deck.</p>
      <p>Specific details about the different card types are discussed in more detail below.</p>
      <p>Each card is telescoped in order to save space on the page. It might be necessary to click on the <i>More</i> or <i> Less </i> links in order to view the rest of your mentoring report or note.</p>
      <p><strong>Cards are ordered by the date on which the card was submitted, and not the date on which a session may have taken place.</strong></p>
      <br>
      <div class="text-center">
        <img src="https://i.img.ie/sqS.png" alt="sqS.png" border="0" class="img-responsive center-block"/>
        <br>
        <strong>Fig. 1.5.2: Deck of Cards</strong>
        <br>
        <hr>
      </div>
      <h6 id="1.5.3">1.5.3 Card Types</h6>
      <p>There are three card types VATeir training system at the moment. Mentoring reports, notes, and information cards (info cards).</p>
      <br>
      <ul>
        <li><h6 id="1.5.3.1">1.5.3.1 Reports</h6></li>
        <p>Mentoring reports are submitted by mentors after having completed a session with you. Mentors can add their own text-based comments, and can mark you against critera set forth in the VATeir Training Syllabus.</p>
        <p>Syllabus criteria can be accessed by clicking on the <i>More</i> link. Mousing over the bar will display your mark for that syllabus item. Mobile devices simply display the mark without the bar.</p>
        <br>
        <div class="text-center">
          <img src="https://i.img.ie/sq5.png" border="0" class="img-responsive center-block"/>
          <br>
          <strong>Fig. 1.5.3: Syllabus Criteria for a Sweatbox session (brown card)</strong>
          <br>
          <hr>
        </div>
        <ul>
          <li><h6 id="1.5.3.1.1">1.5.3.1.1 Live</h6></li>
          <p>A <i>Live</i> mentoring report is for sessions which take place live on the VATSIM network.</p>
          <br>
          <div class="text-center">
            <img src="https://i.img.ie/sqU.png" border="0" class="img-responsive center-block"/>
            <br>
            <strong>Fig. 1.5.4: Live Report</strong>
            <br>
            <hr>
          </div>
          <li><h6 id="1.5.3.1.2">1.5.3.1.2 Sweatbox</h6></li>
          <p>A <i>Sweatbox</i> mentoring report is for sessions which take place offline on the sweatbox server.</p>
          <br>
          <div class="text-center">
            <img src="https://i.img.ie/sqi.png" border="0" class="img-responsive center-block"/>
            <br>
            <strong>Fig. 1.5.5: Sweatbox Report</strong>
            <br>
            <hr>
          </div>
          <li><h6 id="1.5.3.1.3">1.5.3.1.3 Exam</h6></li>
          <p>An <i>Exam</i> report is for practical exams.</p>
          <br>
          <div class="text-center">
            <img src="https://i.img.ie/sq4.png" border="0" class="img-responsive center-block"/>
