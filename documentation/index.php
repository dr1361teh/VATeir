            <br>
            <hr>
          </div>
        </ul>
        <li><h6 id="1.5.3.2">1.5.3.2 Notes</h6></li>
        <p>Notes allow a mentor to add information such as the issuance of theory tokens, validations, theory exam pass/fail results, or any other information that would be relevant to your training history in the VATeir.</p>
        <br>
        <ul>
          <li><h6 id="1.5.3.2.1">1.5.3.2.1 General</h6></li>
          <p>A <i>General</i> note ("Note" on your training history) is for mentors to add miscellaneous information to your training history.</p>
          <br>
          <div class="text-center">
