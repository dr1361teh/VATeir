      <h6 id="2.4.1">2.4.1 What is it?</h6>
      <p>The validations feature allows the training department to keep track of everyone's validations. Mentors can submit a validation request on behalf of a student, however the must be approved by a member of TD Staff before it comes in to effect and before it shows up in a student's list of validations list.</p>
      <br>
      <h6 id="2.4.2">2.4.2 Views</h6>
      <p>The validation list is split up into two views. The default view is the <i>Sector</i> view, whereas to access the <i>Position</i> view, a mentor must click on the <i>Position View</i> link at the top right of the list of validations.</p>
      <br>
      <ul>
        <h6 id="2.4.2.1">2.4.2.1 Sector View</h6>
        <p>The sector view offers a 'top-down' representation of a student's validations. For example, if a student is shown to be validated on EIDW_TWR, that means that they are also validated on EIDW_DEL and EIDW_GND. This is to ensure that all validations are explicity stated in the event of queries from VATSIM supervisors over the validity of a student's validation.</p>
