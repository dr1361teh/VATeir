      <p>In order to submit your availability, select <i>Availability</i> from the menu on the left-hand side of the page.</p>
      <ol>
        <li>
          <p>Firstly, select a date on which you will be available for mentoring. Click on the <strong>calendar icon</strong> to the right of the text box to open the calendar.</p>
