  <h5 id="1.4">1.4 Validations</h5>
  <div class="panel panel-default">
    <div class="panel-body">
      <h6 id="1.4.1">1.4.1 What is it?</h6>
      <p>Validations are a means of allowing VATSIM members who are not pre&ndash;authorised to log on and control a position to do so. These validations include Solo Tower Validations (STVs).</p>
      <p>In the course of their training, a VATeir student may be given an STV or other relevant validation before they have attained the requisite VATSIM rating to log on to a position.</p>
      <p>The student's mentor submits the validation on the student's behalf. This must then be approved by a member of the staff of the training department before the validation is considered active. At this point it will display on the student's <i>Validations</i> page and on the list of validated students available to VATSIM supervisors.</p>
      <hr>
      <h6 id="1.4.2">1.4.2 Receving a Validation</h6>
      <p>The validation section requires no student input as everything is handled by the training department.</p>
      <br>
