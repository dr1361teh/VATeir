<h3 class="text-center">About us</h3>
<p>The Virtual Air Traffic SIMulation (VATSIM) network is a global community in which, through a variety of software packages, aviation enthusiasts, professionals or hobbyists, connect together online to emulate their real-life countarparts, either as Air Traffic Controllers, or as pilots.</p>

<p>Founded in July 2001 and based largely upon the Microsoft Flight Simulator Series, VATSIM now has over 76,000 active members as of 2014, and continues to thrive and grow daily. VATSIM aimed to create a global community for learning, sharing experiences and pursuing new opportunities in the virtual world,
and continues to endorse this behaviour.</p>

<p>The VATSIM world is divided into Regions, then Divisions and finally local facilities, which take on a number of different names depending on where you are in the world. These structures help to build and maintain communities across the globe, providing resources, training and a place to share the experience
of VATSIM wherever you are. VATSIM is also the main network for many established Virtual Airlines and other virtual flying groups, whose pilots populate our skies every day with hundreds of flights.</p>

<p>vACC Ireland is a sub-division of VATSIM: Europe division, which is a division within the VATSIM Europe region. We're responsible for providing air traffic services within the Shannon FIR, nestled between Scotland, Wales, England, France and the Oceanic airspace.</p>

